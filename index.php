	<!-- Asas -->
	<div class="card-horizontal d-flex flex-nowrap justify-content-around my-5">
		<img src="img/asas-bg.png" data-aos="zoom-in-up" data-aos-duration="1000">
		<div class="text-cont d-flex flex-column px-2" data-aos="zoom-in-down" data-aos-duration="1000">
			<p class="title">Asas</p>
			<p class="text">Leonardo Da Vinci não foi capaz de reproduzir o movimento dos pássaros em seus projetos, entretanto, a observação dos animais trouxe conceitos importantes para a aerodinâmica que posteriormente ajudariam na criação do avião muitos séculos depois.</p>
		</div>
	</div>

	<!-- Paraquedas -->
	<div class="card-horizontal d-flex flex-nowrap justify-content-around my-5">
		<img src="img/paraquedas.png" data-aos="zoom-in-up" data-aos-duration="1000">
		<div class="text-cont d-flex flex-column px-2" data-aos="zoom-in-down" data-aos-duration="1000">
			<p class="title">Paraquedas</p>
			<p class="text">Muito antes da invenção do avião, Leonardo Da Vinci já pensava em maneiras de cair suavemente de grandes alturas. O equipamento é semelhante ao paraquedas atual, entretanto, ele possui um formato de pirâmide. A ideia foi aprimorada e ganhou grande utilidade séculos depois, principalmente a partir da Segunda Guerra Mundial.</p>
		</div>
	</div>

	<!-- Tanque -->
	<div class="card-horizontal d-flex flex-nowrap justify-content-around my-5">
		<img src="img/tanque.png" data-aos="zoom-in-up" data-aos-duration="1000">
		<div class="text-cont d-flex flex-column px-2" data-aos="zoom-in-down" data-aos-duration="1000">
			<p class="title">Tanque</p>
			<p class="text">Com formato similar ao de um disco voador, o tanque de Leonardo Da Vinci seria um veículo de guerra equipado com canhões que atingiam um ângulo de 360°. A cobertura convexa tinha como finalidade desviar do fogo inimigo. O veículo tinha capacidade para oito pessoas. Quatro séculos depois, serviria de modelo para a criação do tanque blindado usado durante a Primeira Guerra Mundial.</p>
